class Subject:
    def __init__(self, educationalProgram, subjectName, semesters,
                 typeOfClass, frequency, teacher, amountOfGroups):
        self.educationalProgram = educationalProgram
        self.subjectName = subjectName
        self.semesters = semesters
        self.typeOfClass = typeOfClass
        self.frequency = frequency
        self.teacher = teacher
        self.amountOfGroups = amountOfGroups

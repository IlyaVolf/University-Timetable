package entities;

public class GeneratedEntity {
    public String specialization;
    public String day;
    public String subjectName;
    public String teacherName;
    public String auditory;
    public String groups;
    public String numberOfClass;
    public String typeOfClass;

    public GeneratedEntity(String specialization, String day, String subjectName, String teacherName,
                           String auditory, String groups, String numberOfClass, String typeOfClass) {
        this.specialization = specialization;
        this.day = day;
        this.subjectName = subjectName;
        this.teacherName = teacherName;
        this.auditory = auditory;
        this.groups = groups;
        this.numberOfClass = numberOfClass;
        this.typeOfClass = typeOfClass;
    }
}
